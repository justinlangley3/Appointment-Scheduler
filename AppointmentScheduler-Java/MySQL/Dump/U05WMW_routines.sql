		INNER JOIN
			`city` as ci
				ON addr.cityId = ci.cityId
	WHERE (MONTH(app.beginTime) = MONTH(NOW()))
    ORDER BY
		ci.cityName ASC,
        app.beginTime ASC;
        
END ;;
